    <h1>Privacy Policy</h1>
    <p>
        This privacy policy outlines how we handle data within the vaach.org Action,
        which integrates with the vaach.org API and allows users to provide optional reference documents.
    </p>
    <h2>Data Collection</h2>
    <ul>
        <li>
            <strong>API Data:</strong> This action uses the vaach.org API to retrieve linguistic data
            (phrases and translations) for specified low-resource languages. No personal data is sent to or stored by the API.
        </li>
        <li>
            <strong>User-Provided Documents:</strong> Users may optionally upload reference documents containing
            linguistic data or translations. These documents are processed temporarily to enhance the translation accuracy.
        </li>
    </ul>
    <h2>Data Usage</h2>
    <ul>
        <li>
            Data retrieved from the vaach.org API is used solely to generate translations for the requested phrases.
        </li>
        <li>
            User-provided reference documents are used as supplementary sources for translation tasks and are prioritized over API data where applicable.
        </li>
        <li>
            Unknown words in translations are represented using placeholders (e.g., <code>{word}</code>).
        </li>
    </ul>
    <h2>Data Retention</h2>
    <ul>
        <li>
            API responses and user-provided documents are processed temporarily during the translation session and are not stored permanently.
        </li>
    </ul>
    <h2>Data Sharing</h2>
    <ul>
        <li>
            No data retrieved from the API or user-provided documents is shared with third parties.
        </li>
    </ul>
    <h2>User Responsibility</h2>
    <ul>
        <li>
            Users are responsible for ensuring the accuracy and appropriateness of any documents they provide.
        </li>
    </ul>
    <h2>Contact</h2>
    <p>
        If you have any questions or concerns about this privacy policy, please contact madhav@vaach.org.
    </p>
